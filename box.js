// Функція для випадкового дропа предметів зі своїми шансами
// coin.png — 70% шанс
// gem.png — 40% шанс
// pin2.png — якщо нічого не випало або для кастомної логіки (наприклад, особливий дроп)

function getDrop() {
    // Генеруємо випадкове число від 0 до 1
    const rand = Math.random();

    // 70% — coin.png, 40% — gem.png
    // Якщо потрібно, щоб кілька предметів могли випадати одночасно, використовуйте окремі перевірки
    // Якщо потрібно лише один дроп — перевіряємо по черзі

    if(rand < 0.4) {
        return "gem.png";
    } else if(rand < 0.4 + 0.3) {
        return "coin.png";
    } else {
        return "pin2.png";
    }
}

// Приклад використання:
function openBox() {
    const drop = getDrop();
    // Тут можна додати логіку для відображення дропа на екрані
    alert(`Ви отримали: ${drop}`);
}

